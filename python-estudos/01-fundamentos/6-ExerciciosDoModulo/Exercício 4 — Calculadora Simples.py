"""

Peça dois números.

Mostre:

soma
subtração
multiplicação
divisão
divisão inteira
resto
potência

"""

numero1 = int(input("Informe um números: "))
numero2 = int(input("Informe outro números: "))

print(f"A soma de {numero1} + {numero2} = {numero1 + numero2}")
print(f"A subtração de {numero1} - {numero2} = {numero1 - numero2}")
print(f"A multiplicação de {numero1} X {numero2} = {numero1 * numero2}")
print(f"A divisão de {numero1} / {numero2} = {numero1 / numero2}")
print(f"A divisão inteira de {numero1} // {numero2} = {numero1 // numero2}")
print(f"A resto de {numero1} % {numero2} = {numero1 % numero2}")
print(f"A potencia de {numero1} ** {numero2} = {numero1 ** numero2}")
