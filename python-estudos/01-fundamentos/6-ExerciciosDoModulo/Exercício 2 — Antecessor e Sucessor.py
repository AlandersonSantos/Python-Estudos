"""
Leia um número inteiro.

Mostre:

antecessor
número informado
sucessor

"""

numero = int(input("Informe um número: "))

antecessor = numero - 1
sucessor = numero + 1

print(f"O antecessor do seu número é {antecessor}")
print(f"Seu número escolhido foi {numero}")
print(f"O sucessor do seu número é {sucessor}")